// Shuffle and return all unique items without duplication
export const getGalleryItems = (type: 'architecture' | 'art' | 'nature' | 'mixed'): ImageItem[] => {
  let baseItems: ImageItem[] = [];
  if (type === 'architecture') baseItems = ARCHITECTURE_GALLERY;
  else if (type === 'art') baseItems = ART_AND_PAINTING_GALLERY;
  else if (type === 'nature') baseItems = NATURE_GALLERY;
  else baseItems = [...ARCHITECTURE_GALLERY, ...ART_AND_PAINTING_GALLERY, ...NATURE_GALLERY];

  const shuffled = [...baseItems];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
};
